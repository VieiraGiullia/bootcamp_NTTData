# Operadores de associação
# são utilizados para verificar se um objeto está presente em outro objeto

rua = 'Rua 1'
carros = ['carro1', 'carro2', 'carro3']
valores = [1, 2, 3, 4, 5]

"Otacilio" in rua # verifica se a string 'Otacilio' está presente na variável rua

"carro4" not in carros # verifica se a string 'carro4' não está presente na lista carros

8 in valores # verifica se o número 8 está presente na lista valores