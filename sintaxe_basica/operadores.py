# operadores aritmeticos 
# sõa utilizados para realizar operações matemáticas

# adição
print(2 + 2)

#subtração
print(2 - 2)

#multiplicação
print(2 * 2)

#divisão e divisão inteira
print(12 / 3)
print(12 // 2)

#modulo e exponenciação
print(10 % 3)
print(2 ** 3)

# precedência de operadores (é igula a matemática)
print(2 + 3 * 4)
print((2 + 3) * 4)

# Operadores de comparação
# são utilizados para comparar valores e retornar um valor booleano

# igualdade
saldo = 450
saque = 400
print(saldo == saque)

# diferença
print(saldo != saque)

# maior que / maior ou igual
print(saldo > saque)
print(saldo >= saque)

# menor que / menor ou igual
print(saldo < saque)
print(saldo <= saque)
