# Operadores de comparação
# são utilizados para comparar valores e retornar um valor booleano

# igualdade
saldo = 450
saque = 400
print(saldo == saque)

# diferença
print(saldo != saque)

# maior que / maior ou igual
print(saldo > saque)
print(saldo >= saque)

# menor que / menor ou igual
print(saldo < saque)
print(saldo <= saque)